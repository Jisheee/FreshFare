    private void loadTextFile() {
        try {
            String usernameFilePath = "C:\\Jishe\\Villaruz_JohnCarl\\Grocery\\TOP SECRET\\VERY CONFIDENTIAL\\LIKE SERIOUSLY\\DONT CLICK\\Admin\\Temporary Container.txt";
            BufferedReader usernameReader = new BufferedReader(new FileReader(usernameFilePath));
            String username = usernameReader.readLine(); // Read the username from the file
            usernameReader.close();
            
            String directoryPath = "C:\\Jishe\\Villaruz_JohnCarl\\Grocery\\TOP SECRET\\VERY CONFIDENTIAL\\LIKE SERIOUSLY\\DONT CLICK\\Customer Credentials";
            
            String filePath = directoryPath + File.separator + username + ".txt";
            
            File file = new File(filePath);
            if (file.exists()) {
                BufferedReader reader = new BufferedReader(new FileReader(filePath));
                
                reader.readLine();

                String line;
                StringBuilder textContent = new StringBuilder();
                while ((line = reader.readLine()) != null) {
                    textContent.append(line).append("\n");
                }
                reader.close();
                acc.setText(textContent.toString()); 
            } else {
                acc.setText("No account details found for " + username); 
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
    }


